    public float selectFinalResult(int idPerson){
        float idP= 0f;
        try {
            String sql = "select finalResult from students where idStudent = "+idPerson+";";
            dataAccessLayer dal= new dataAccessLayer();
            dal.stmt = dal.conn.createStatement();
            dal.rs= dal.stmt.executeQuery(sql);
            
            if(dal.rs.next()){
                idP = dal.rs.getFloat(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
        }
        return idP;
        
    }
    public  void updateTable(){
      String sql="select module, coeff, participation, interrogation1, interrogation2, tp, exam , resultModule\n" +
              "from modules md left join specializations_modules spm on (md.idModule = spm.idModule) left join students st on (spm.idSpecialization = st.idSpecialization) left join notes1 n1 on (st.idStudent = n1.idStudent and md.idModule = n1.idModule) left join notes2 n2 on (n1.idNote1 = n2.idNote1) left join notes3 n3 on (n1.idNote1 = n3.idNote1) left join students_modules stm on (n1.idStudent = stm.idStudent and n1.idModule = stm.idModule) \n" +
              "where st.idStudent = "+this.idPerson+";";
        dataAccessLayer dal = new dataAccessLayer();
      try{
      dal.stmt=dal.conn.createStatement();
      dal.rs=dal.stmt.executeQuery(sql);
      System.out.println("idPerson from updateTable is "+ this.idPerson);
      
      jTable1.setModel(DbUtils.resultSetToTableModel(dal.rs));
      
      }catch(Exception ex){
      System.out.println("failed in updateTable");
      }
    }
